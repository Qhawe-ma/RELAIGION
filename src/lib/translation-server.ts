/**
 * Translates English text to Chinese using keyword replacement
 * Runs server-side when AI messages are created
 */
export function translateToChinese(text: string): string {
  if (!text) return "";
  
  let translated = text;
  
  // Sort by length (longest first) to avoid partial replacements
  const entries = Object.entries(aiPhraseDictionary).sort((a, b) => b[0].length - a[0].length);
  
  for (const [english, chinese] of entries) {
    // Case-insensitive replacement with word boundaries
    const escaped = english.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    const regex = new RegExp(`\\b${escaped}\\b`, 'gi');
    translated = translated.replace(regex, chinese);
  }
  
  return translated;
}

/**
 * Creates a message object with both English and Chinese text
 */
export function createBilingualMessage(
  bot: string,
  model: string,
  text: string,
  timestamp: number
) {
  return {
    bot,
    model,
    text,           // English (original)
    textZh: translateToChinese(text),  // Chinese translation
    timestamp,
  };
}
